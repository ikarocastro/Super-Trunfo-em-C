#include <stdio.h>

int main() {
    int idade = 16;

    if(idade >= 16)
        printf("Pode votar!");
    else
        printf("não pode votar!");

    return 0;
}

//vimos que o "if" mostra que se a idade for maior ou igual a 16, vai exibir que pode votar, o "else" que é "Se não" vai exibir que não pode votar!