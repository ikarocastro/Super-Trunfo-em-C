#include <stdio.h>

int main() {

    //> (maior que)
    //< (menor que)
    //>= (maior ou igual a)
    //<= (menor ou igual a)
    //== (igual a)
    //!= (diferente de)
    
    int a = 10;
    int b = 20;

    printf("a > b: %d\n", a > b); //False = 0
    printf("a < b: %d\n", a < b);  //True = 1
    printf("a == b: %d\n", a == b); //False = 0
    printf("a != b: %d\n", a != b); //True = 1

    
    return 0;

}